#!/usr/bin/env python


def divpots(pota, potb):
    """Return the result of pota / potb.

    Parameters :
        pota : Potential :
            The dividend potential.

        potb : Potential :
            The divisor potential.

    Returns :
        newpot : Potential :
            The new pot equals to pota / potb.

    Raises :
        None

    Notes :
        None
    """
    newpot = pota / potb
    return newpot
